package CH_6_Functions;

public class _5_Swapping {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;
        int swap = a;
        a = b;
        b = swap;
        System.out.println("Your swap value of A = " +a);
        System.out.println("Your swap value of B = " +b);
    }
}
